<?php 
/* pengulangan dalam php ada 
- for, while, do ... while, foerach (khusus untuk array)
*/
//menggunakan for
for($i = 0; $i < 5; $i++){
	echo "menggunakan for <br>";
}

//menggunakan while
$r = 0;
while($r < 6 ){
	echo "menggunakan while <br>";
$r++;
}

//menggunakan do while
$c = 0;
do{
	echo "menggunakan do while <br>";
$c++;	
}while($c<7);

 ?>